package loops;

public class forloop {
    public static void main(String[] args) {
        // For loop example
        for (int i = 0; i < 5; i++) {
            System.out.println("Hello World " + i);
        }
        System.out.println("Loop ended with i = 5");
        
        // The loop will execute 5 times, printing "Hello World" followed by the current value of i
    }
}
